export type DetailTab = "scan" | "details" | "related";
export const DETAIL_TABS: { key: DetailTab; label: string }[] = [
  { key: "scan", label: "Quick Scan" }, { key: "details", label: "Details" }, { key: "related", label: "Related" },
];

// Parses the leading page number out of a section's "pages" string ("8-15" -> 8, "37" -> 37) for
// jumping the PDF viewer there.
export const firstPage = (pages: string): number => parseInt(pages, 10) || 1;

// Renders PDF pages ourselves (pdfjs-dist, canvas) with our own Prev/Next controls, rather than
// relying on the browser's native PDF plugin inside an <iframe> -- that approach looked fine on
// desktop Chrome but showed no toolbar/controls at all on mobile/touch browsers, since the full
// PDF.js viewer chrome is a desktop-Chrome-only feature, not a guaranteed <iframe> behavior.
export const PdfViewer = ({ url, page, onPageChange, tall }: { url: string; page: number; onPageChange: (p: number) => void; tall: boolean }) => {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [pdfDoc, setPdfDoc] = useState<PdfjsLib.PDFDocumentProxy | null>(null);
  const [pageCount, setPageCount] = useState(0);

  useEffect(() => {
    let cancelled = false;
    setPdfDoc(null);
    setPageCount(0);
    loadPdfjs().then(pdfjsLib => pdfjsLib.getDocument({ url }).promise).then(pdf => {
      if (cancelled) return;
      setPdfDoc(pdf);
      setPageCount(pdf.numPages);
    });
    return () => { cancelled = true; };
  }, [url]);

  useEffect(() => {
    if (!pdfDoc || !canvasRef.current) return;
    let cancelled = false;
    const clamped = clamp(page, 1, pdfDoc.numPages);
    pdfDoc.getPage(clamped).then(pdfPage => {
      if (cancelled || !canvasRef.current) return;
      const canvas = canvasRef.current;
      const containerWidth = canvas.parentElement?.clientWidth || 600;
      const unscaled = pdfPage.getViewport({ scale: 1 });
      const viewport = pdfPage.getViewport({ scale: containerWidth / unscaled.width });
      canvas.width = viewport.width;
      canvas.height = viewport.height;
      const ctx = canvas.getContext("2d");
      if (!ctx) return;
      pdfPage.render({ canvasContext: ctx, viewport });
    });
    return () => { cancelled = true; };
  }, [pdfDoc, page, tall]);

  return (
    <div className="mt-1 overflow-hidden rounded-lg border border-slate-200 dark:border-slate-700">
      <div className="flex items-center justify-between gap-2 border-b border-slate-200 dark:border-slate-700 bg-slate-50 dark:bg-slate-900 px-3 py-2">
        <button onClick={() => onPageChange(page - 1)} disabled={page <= 1} title="Previous page"
          className="grid h-8 w-8 place-items-center rounded-lg text-slate-500 dark:text-slate-400 disabled:opacity-30">
          <ChevronLeft size={16} />
        </button>
        <span className="text-xs font-bold" style={{ color: MUTED }}>Page {page} of {pageCount || "-"}</span>
        <button onClick={() => onPageChange(page + 1)} disabled={!pageCount || page >= pageCount} title="Next page"
          className="grid h-8 w-8 place-items-center rounded-lg text-slate-500 dark:text-slate-400 disabled:opacity-30">
          <ChevronRight size={16} />
        </button>
      </div>
      <div className={`overflow-auto bg-slate-100 dark:bg-slate-950 ${tall ? "h-[calc(100vh-200px)]" : "h-[60vh]"}`}>
        <canvas ref={canvasRef} className="mx-auto block" />
      </div>
    </div>
  );
};

export const DocumentDetailPanel = ({ doc, allDocs, tab, onTabChange, onSelectRelated, expanded, onToggleExpand, layoutMode }: {
  doc: EduDocument; allDocs: EduDocument[]; tab: DetailTab; onTabChange: (t: DetailTab) => void;
  onSelectRelated: (id: string) => void;
  expanded: boolean; onToggleExpand: () => void; layoutMode: EffectiveLayout;
}) => {
  const [currentPage, setCurrentPage] = useState(1);
  useEffect(() => setCurrentPage(1), [doc.id]);
  const related = allDocs.filter(d => d.id !== doc.id && d.category === doc.category).slice(0, 4);
  // Maximised on desktop gets room for a side-by-side layout; maximised on phone (or
  // not maximised at all) keeps the PDF viewer and contents stacked in one column.
  const sideBySide = expanded && layoutMode === "web";
  // While side-by-side, the doc info/actions/tabs move into the sticky contents column
  // next to "Sections in this guide" instead of sitting above the two-column grid -- but
  // only for the Quick Scan tab, which is the only one that renders that grid; Details/
  // Related still render single-column with the header on top, same as non-maximised.
  const headerInSidebar = sideBySide && tab === "scan" && !!doc.fileUrl;

  const header = (
    <>
      <div className="mb-3 flex items-center justify-between gap-2">
        <span className="truncate text-xs font-bold uppercase tracking-widest" style={{ color: MUTED }}>Document viewer</span>
        <button onClick={onToggleExpand} title={expanded ? "Exit full screen" : "Full screen"}
          className="grid h-8 w-8 shrink-0 place-items-center rounded-lg border border-slate-200 dark:border-slate-700 text-slate-400 dark:text-slate-500">
          {expanded ? <Minimize2 size={14} /> : <Maximize2 size={14} />}
        </button>
      </div>
      <div className="h-32 rounded-lg grid place-items-center" style={{ background: doc.swatch }}>
        <FileText size={32} color={WHITE} />
      </div>
      <div className="mt-3 text-base font-bold" style={{ color: NAVY }}>{doc.title}</div>
      <span className={eduBadgeCx + " mt-1.5 inline-block"}>{doc.category}</span>
      <div className="mt-3 space-y-1">
        <Row k="Edition" v={doc.edition} dim />
        <Row k="Date" v={doc.date} dim />
        <Row k="File" v={`${doc.fileType} · ${doc.fileSize}`} dim />
      </div>
      <div className="mt-3 flex items-center gap-2">
        {doc.fileUrl ? (
          <a href={doc.fileUrl} target="_blank" rel="noopener noreferrer"
            className="flex-1 rounded-xl py-2.5 text-center text-sm font-bold" style={{ background: BLUE, color: WHITE }}>Open PDF</a>
        ) : (
          <button className="flex-1 rounded-xl py-2.5 text-sm font-bold" style={{ background: BLUE, color: WHITE }}>Open PDF</button>
        )}
        <button className="grid h-10 w-10 place-items-center rounded-xl border border-slate-200 dark:border-slate-700 text-slate-400 dark:text-slate-500"><Share2 size={15} /></button>
        <button className="grid h-10 w-10 place-items-center rounded-xl border border-slate-200 dark:border-slate-700 text-slate-400 dark:text-slate-500"><MoreVertical size={15} /></button>
      </div>
    </>
  );

  const tabsNav = (
    <div className="mt-4 grid grid-cols-3 gap-1 rounded-xl border border-slate-200 dark:border-slate-700 p-1">
      {DETAIL_TABS.map(t => (
        <button key={t.key} onClick={() => onTabChange(t.key)}
          className={"rounded-lg py-2 text-xs font-bold transition-all " + (tab === t.key ? "" : "text-slate-400 dark:text-slate-500")}
          style={tab === t.key ? { background: BLUE, color: WHITE } : undefined}>
          {t.label}
        </button>
      ))}
    </div>
  );

  return (
    <div className={cx.card}>
      {!headerInSidebar && (
        <>
          {header}
          {tabsNav}
        </>
      )}
      <div className={headerInSidebar ? "" : "mt-4"}>
        {tab === "scan" && (
          doc.fileUrl ? (
            sideBySide ? (
              <div className="grid grid-cols-[1fr_320px] gap-6 items-start">
                <PdfViewer key={doc.id} url={doc.fileUrl} page={currentPage} onPageChange={setCurrentPage} tall={expanded} />
                <div className="sticky top-4 max-h-[calc(100vh-200px)] overflow-y-auto">
                  {header}
                  {tabsNav}
                  <div className={cx.cardHd + " mt-4"}>Sections in this guide</div>
                  <SectionsList sections={doc.sections} onOpenSection={pages => setCurrentPage(firstPage(pages))} />
                </div>
              </div>
            ) : (
              <>
                <PdfViewer key={doc.id} url={doc.fileUrl} page={currentPage} onPageChange={setCurrentPage} tall={expanded} />
                <div className={cx.cardHd + " mt-4"}>Sections in this guide</div>
                <SectionsList sections={doc.sections} onOpenSection={pages => setCurrentPage(firstPage(pages))} />
              </>
            )
          ) : (
            <>
              <div className={cx.cardHd}>About this guide</div>
              <p className="text-sm leading-relaxed" style={{ color: MUTED }}>{doc.description}</p>
              <div className={cx.cardHd + " mt-4"}>Sections in this guide</div>
              <SectionsList sections={doc.sections} />
            </>
          )
        )}
        {tab === "details" && (
          <div className="space-y-1">
            <Row k="Pages" v={doc.pageCount} dim />
            <Row k="Tags" v={doc.tags.join(", ")} dim />
            <Row k="Category" v={doc.category} dim />
          </div>
        )}
        {tab === "related" && (
          related.length === 0
            ? <p className={cx.footnote}>No related documents in this category.</p>
            : <div className="space-y-2">
                {related.map(d => (
                  <button key={d.id} onClick={() => onSelectRelated(d.id)}
                    className="w-full rounded-xl border border-slate-200 dark:border-slate-700 px-3.5 py-2.5 text-left text-sm font-semibold" style={{ color: NAVY }}>
                    {d.title}
                  </button>
                ))}
              </div>
        )}
      </div>
    </div>
  );
};

export const EducationHub = ({ layoutMode }: { layoutMode: EffectiveLayout }) => {
  const [query, setQuery] = useState("");
  const [category, setCategory] = useState<EduCategory>("All");
  const [selectedId, setSelectedId] = useState<string>(EDU_DOCUMENTS[0].id);
  const [recentIds, setRecentIds] = useState<string[]>([]);
  const [detailTab, setDetailTab] = useState<DetailTab>("scan");
  // Grid + a persistent detail panel (aside on web, stacked below on phone) is always the
  // default view -- expanded is purely an opt-in "make the viewer bigger" toggle, not a
  // separate mode the user has to enter/exit to see anything.
  const [expanded, setExpanded] = useState(false);

  const filtered = useMemo(() => {
    const q = query.trim();
    const byCategory = (d: EduDocument) => category === "All" || d.category === category;
    if (!q) return EDU_DOCUMENTS.filter(byCategory);
    // Full-text search (title/tags/category/description/extracted PDF text) via the
    // pre-built MiniSearch index -- see scripts/add-education-doc.mjs.
    const matchIds = new Set(eduSearchIndex.search(q, { prefix: true, fuzzy: 0.2, boost: { title: 3, tags: 2 } }).map(r => r.id));
    return EDU_DOCUMENTS.filter(d => byCategory(d) && matchIds.has(d.id));
  }, [query, category]);

  const selectDoc = (id: string) => {
    setSelectedId(id);
    setRecentIds(prev => [id, ...prev.filter(x => x !== id)].slice(0, 6));
    setDetailTab("scan"); // every selection path lands on the Quick Scan view.
  };

  const openQuickScan = (id: string) => {
    selectDoc(id);
    setExpanded(true);
  };

  const selectedDoc = EDU_DOCUMENTS.find(d => d.id === selectedId) ?? EDU_DOCUMENTS[0];

  const gridBody = (
    <>
      <div className="mt-4 flex items-center gap-2 rounded-xl border border-slate-200 dark:border-slate-700 bg-white dark:bg-slate-800 px-4 py-3 shadow-sm">
        <Search size={16} className="shrink-0" style={{ color: MUTED }} />
        <input value={query} onChange={e => setQuery(e.target.value)} placeholder="Search guides, tags, categories..."
          className="w-full bg-transparent text-sm outline-none" style={{ color: NAVY }} />
      </div>
      <div className="mt-3"><FilterChips active={category} onChange={setCategory} /></div>
      <RecentlyViewedStrip ids={recentIds} docs={EDU_DOCUMENTS} onSelect={selectDoc} />
      <div className="mt-5"><SectionLabel icon={<FileText size={14} />}>All Documents ({filtered.length})</SectionLabel></div>
      {filtered.length === 0 ? (
        <div className={cx.card + " mt-3 text-center"}>
          <p className={cx.footnote}>No documents match your search.</p>
        </div>
      ) : (
        <CardGrid layoutMode={layoutMode} minWidth={280}>
          {filtered.map(d => (
            <DocumentCard key={d.id} doc={d} selected={d.id === selectedId} onSelect={selectDoc} onQuickScan={openQuickScan} />
          ))}
        </CardGrid>
      )}
    </>
  );

  const detailPanel = (
    <DocumentDetailPanel
      doc={selectedDoc} allDocs={EDU_DOCUMENTS} tab={detailTab} onTabChange={setDetailTab} onSelectRelated={selectDoc}
      expanded={expanded} onToggleExpand={() => setExpanded(v => !v)} layoutMode={layoutMode}
    />
  );

  // The wrapper around detailPanel keeps the same element/position across the expanded
  // toggle -- only its className changes -- so React preserves DocumentDetailPanel's (and
  // PdfViewer's) component state (current page, loaded PDF) instead of remounting it and
  // losing all of that whenever full screen is toggled on/off.
  const expandedClass = "fixed inset-0 z-50 overflow-y-auto bg-slate-50 p-4 dark:bg-slate-950 md:p-8";

  if (layoutMode === "phone") {
    return (
      <div className="mt-2">
        {gridBody}
        <div className={expanded ? expandedClass : "mt-6"}>{detailPanel}</div>
      </div>
    );
  }

  return (
    <div className="mt-2 grid grid-cols-[1fr_380px] items-start gap-6">
      <div className="min-w-0">{gridBody}</div>
      <aside className={expanded ? expandedClass : "sticky top-5"}>{detailPanel}</aside>
    </div>
  );
};
